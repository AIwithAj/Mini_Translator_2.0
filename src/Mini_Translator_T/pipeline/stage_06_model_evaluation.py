from Mini_Translator_T.config.configuration import ConfigurationManager
from Mini_Translator_T.components.model_evaluation import Evaluation
from Mini_Translator_T.logging import logger



STAGE_NAME = "Evaluation stage"


class EvaluationPipeline:
    def __init__(self):
        pass

    def main(self):
        config = ConfigurationManager()
        eval_config = config.get_eval_model_config()
        evaluation = Evaluation(eval_config)
        evaluation.initiate_model_Evaluation()
   




if __name__ == '__main__':
    try:
        logger.info(f"*******************")
        logger.info(f">>>>>> stage {STAGE_NAME} started <<<<<<")
        obj = EvaluationPipeline()
        obj.main()
        logger.info(f">>>>>> stage {STAGE_NAME} completed <<<<<<\n\nx==========x")
    except Exception as e:
        logger.exception(e)
        raise e
            